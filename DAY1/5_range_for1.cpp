#include <vector>

int main()
{
	std::vector<int> v = { 1,2,3,4,5 };

	// 전통적인 for 문
	for (int i = 0; i < v.size(); i++)
	{
	}

	// C++11 에서 새로 나온 for 문
	// 파이썬 : for n in v
	// C#    : foreach int n in v
	for (int n : v)
	{

	}


}















